const mongoose = require('mongoose');

const adminSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true
  },
  token: {
    type: String, // Used for authentication (you already store token in localStorage)
    required: true
  }
});

module.exports = mongoose.model('Admin', adminSchema);
